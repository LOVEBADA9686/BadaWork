package a.b.c.ch1;

public class Ex_2{

	//main 함수 : 콘솔 어플리케이션의 시작점
	public static void main(String args[]){
		System.out.println("EX_2 :: main() 시작 >>> : ");

		// 자바 데이터 타입(자료형) : 기초 자료형 : 숫자 : 정수 : int
		// i : 변수이름 : 변수 명 : 데이터를 담는 통의 이름이 i
		// = : 대입 연산자
		// 1 : 맅터렬 : 값
		// 인트형 데이터타입을 선언하고, 변수명을 i로 선언해서 값 1을 초기화 한다. 대입한다. 넣는다.

		int i = 1;

		// 콘손에 println에 있는 아규먼트 변수 i 값을 출력 하시오
		// println 함수에서 지역변수 i 를 호출(invoke)해서 아규먼트로 받아서 출력한다.
		System.out.println(i);
		// System.out.println(i);

		System.out.println("EX_2 :: main() 끝 >>> : ");
	} // end of main 함수
} // end of Ex_2 클래스